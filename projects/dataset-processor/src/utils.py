"""
utils.py
--------
Utility functions for the dataset conversion pipeline.

    write_dataset_yaml()  — writes a YOLO-compatible dataset.yaml config file
    print_split_summary() — prints a human-readable conversion summary
"""

from __future__ import annotations

import logging
from pathlib import Path

logger = logging.getLogger(__name__)


def write_dataset_yaml(
    output_dir: str | Path,
    filename: str = "dataset.yaml",
) -> Path:
    """
    Write a YOLO-compatible dataset.yaml configuration file.

    This file is required by Ultralytics YOLO (v8, v11, v26) to locate
    the dataset and define the class list.

    Args:
        output_dir: Root dataset directory — the same path passed to the
                    converters as output_dir.
        filename:   Name of the output YAML file. Default: "dataset.yaml".

    Returns:
        Path to the written YAML file.

    Example output:
        path: /absolute/path/to/dataset
        train: images/train
        val:   images/val
        nc: 1
        names:
          0: pedestrian
    """
    output_dir = Path(output_dir)
    yaml_path  = output_dir / filename

    content = f"""\
# YOLO Dataset Configuration
# Generated by dataset_converter/utils.py
#
# Usage with Ultralytics:
#   from ultralytics import YOLO
#   model = YOLO("yolov11s.pt")
#   model.train(data="{yaml_path.resolve()}", epochs=100)

path: {output_dir.resolve()}

train: images/train
val:   images/val

# Number of classes
nc: 1

# Class names
names:
  0: pedestrian
"""

    output_dir.mkdir(parents=True, exist_ok=True)
    yaml_path.write_text(content)
    logger.info("dataset.yaml written to %s", yaml_path)
    return yaml_path


def print_split_summary(
    train_stats: dict[str, int],
    val_stats: dict[str, int],
    dataset_name: str = "Dataset",
) -> None:
    """
    Print a formatted summary table comparing train and val conversion stats.

    Args:
        train_stats: Stats dict returned by converter.convert() for train split.
        val_stats:   Stats dict returned by converter.convert() for val split.
        dataset_name: Label shown in the header row.

    Example output:
        ┌─────────────────────────┬────────┬──────┐
        │ KITTI                   │ train  │  val │
        ├─────────────────────────┼────────┼──────┤
        │ Total images            │  6000  │ 1481 │
        │ With pedestrians        │  3241  │  812 │
        │ Empty (no pedestrians)  │  2759  │  669 │
        │ Skipped (errors)        │     0  │    0 │
        └─────────────────────────┴────────┴──────┘
    """
    rows = [
        ("Total images",           "total"),
        ("With pedestrians",       "with_pedestrians"),
        ("Empty (no pedestrians)", "empty"),
        ("Skipped (errors)",       "skipped"),
    ]

    col_w   = max(len(r[0]) for r in rows) + 2
    num_w   = 8
    header  = f"{'':>{col_w}} {'train':>{num_w}} {'val':>{num_w}}"
    divider = "─" * len(header)

    print(f"\n{dataset_name} conversion summary")
    print(divider)
    print(header)
    print(divider)

    for label, key in rows:
        t = train_stats.get(key, 0)
        v = val_stats.get(key, 0)
        print(f"  {label:<{col_w - 2}} {t:>{num_w}} {v:>{num_w}}")

    print(divider + "\n")